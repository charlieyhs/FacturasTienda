    private void BotonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonAgregarActionPerformed
        // TODO add your handling code here:
        ArchivoSecciones ArchSecciones = new ArchivoSecciones();
        
        ArchSecciones.CrearArchivo();
        
        String seccion;
        //Obtenemos los datos de las cajas de texto
        seccion = CajaSeccion.getText();
        
        //Creamos un objeto de la clase "ClaseFerreteria"
        
        ClaseSecciones ObjSeccion = new ClaseSecciones(seccion);
        
        //escribimos los datos del nuevo producto en nuestro archivo de texto
        
        ArchSecciones.escribirTexto(ObjSeccion);
        
        JOptionPane.showMessageDialog(null, "Secci√≥n Agregada");
        
        CajaSeccion.setText("");
        
        
    }//GEN-LAST:event_BotonAgregarActionPerformed

